// Сгенерировать двумерный массив A произвольных чисел размером 10 на 10: с помощью функции создаем двумерный массив и заполняем его случайными числами от 1 до 20
function gen_array($row, $col) {
    for ($i = 1; $i <= $row; $i++) {
        for ($y = 1; $y <= $col; $y++) {
            $arr[$i][$y] = rand(1, 20);
        }
    }
    return $arr;
}

$A = gen_array(10, 10);

// создаем массив С размером 10 на 10 и заполняем его значением 'no'
function empty_array($row, $col) {
    for ($i = 1; $i <= $row; $i++) {
        for ($y = 1; $y <= $col; $y++) {
            $arr[$i][$y] = 'no';
        }
    }
    return $arr;
}

$C = empty_array(10, 10);

// Вывести результат работы в таблицу: переменные массива А: каждая в свою строку/ячейку: для этого используем функцию show_table

function show_table($arr) {
    echo "<table border='1'>";
    foreach ($arr as $value) {
        echo '<tr>';
        foreach ($value as $val) {
            echo "<td>" . $val . "</td>";
        }
        echo '</tr>';
    }
    echo "</table>";
}

echo "Двумерный массив A произвольных чисел размером 10 на 10:" . "<br />";
