/* Пройтись по массиву A и оставить в нем только числа, которые есть в массиве B: с помощью функции принимаем три массива: А, В и массив С,
заполенный значениями 'no'. Испрользуя 3 вложенных цикла for сравниваем массив А с массивом В,
в случае совпадения значения из массива А с значением из массива В это значение присваиваем в массив С в тоже место как и в массиве А */

function search_array($A, $B, &$C) {
    for ($i = 1; $i <= 10; $i++) {
        for ($y = 1; $y <= 10; $y++) {
            for ($j = 1; $j <= 10; $j++) {
                if ($A[$i][$y] == $B[$j]) {
                    $C[$i][$y] = $B[$j];
                };
            }
        }
    }
    return $C;
}

